	<h2>Admin FAQ</h2>
	
	<p>
	<li>Question: How do I register a student for a class? </li>
	<li>Solution: Click 'search' on the top menu and find their desired room. From there you can click 'Register,' which is to the right of the room and enter their RCSID.</li>
	</p>
	
	<p>
	<li>Question: When registering a student for class I get a message saying that they have already registered? </li>
	<li>Solution: This student is already in the system. If you are sure you would like to register them to a different room then go to the admin panel on the homepage and click 'Check Students Room Status.' Enter their RCSID and click 'lookup' then click 'Remove User.' They are no longer registered for any rooms so they may be registered again.  </li>
	</p>

